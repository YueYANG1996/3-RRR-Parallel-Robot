function q = cal_Ik(P,theta)
    %%%for physical
    %%%Generate the value for servo
    la1 = 11.25;
    la2 = 11.25;
    la3 = 11.25;
    lp1 = 18.75;
    lp2 = 18.75;
    lp3 = 18.75;
    b3 = [0, 0];
    b1 = [30, 0];
    b2 = [15, 15*sqrt(3)];
    rp = 6;
    a1x = P(1) + rp*cos(theta - pi/6);
    a1y = P(2) + rp*sin(theta - pi/6);
    a1 = [a1x, a1y];
    a2x = P(1) + rp*cos(theta + pi/2);
    a2y = P(2) + rp*sin(theta + pi/2);
    a2 = [a2x, a2y];
    a3x = P(1) + rp*cos(theta + 7*pi/6);
    a3y = P(2) + rp*sin(theta + 7*pi/6);
    a3 = [a3x, a3y];
    phi1 = atan2(a1(2) - b1(2), a1(1) - b1(1));
    phi2 = atan2(a2(2) - b2(2), a2(1) - b2(1));
    phi3 = atan2(a3(2) - b3(2), a3(1) - b3(1));
    alpha1 = cal_alpha(a1,b1,la1,lp1);
    alpha2 = cal_alpha(a2,b2,la2,lp2);
    alpha3 = cal_alpha(a3,b3,la3,lp3);
    q1 = phi1 - alpha1;
    q2 = phi2 - alpha2;
    q3 = phi3 - alpha3;
    q11 = phi1 - alpha1;
    q22 = phi2 - alpha2+4*pi/3;
    q33 = 2*pi/3-(alpha3 -phi3);
    e1x = b1(1) + la1*cos(q1);
    e1y = b1(2) + la1*sin(q1);
    e1 = [e1x, e1y];
    e2x = b2(1) + la2*cos(q2);
    e2y = b2(2) + la2*sin(q2);
    e2 = [e2x, e2y];
    e3x = b3(1) + la3*cos(q3);
    e3y = b3(2) + la3*sin(q3);
    e3 = [e3x, e3y];
    q11=q11*180/pi;
    q11=q11/180
    q22=q22*180/pi;
    q22=q22/180
    q33=q33*180/pi;
    q33=q33/180
    q = [q11 q22 q33];
end